
fun main(){
    //변수

    //변수 초기화1
    //선언하고(변수명을 지어야 함)  값을 할당과 함께 동시에

    var num5=7;


    //var : 변수값 변경 가능( 데이터 타입이 동적으로 정해짐)
    var num1=2
    println("hi")
    println(num1)

    num1=5

    println(num1)

    //num1="hi"

    //불변(immutable)한 변수를 선언할 때 사용됩니다. 이 변수는 한 번 초기화되면 그 값을 변경할 수 없습니다.
    //다른 말로하면, val로 선언된 변수는 할당된 값을 변경할 수 없는 상수와 비슷한 동작
    //값(value)을 바꾸지 마라!!
    val num2=10;
    println(num2)

    //num2=15
    val PI=3.14159
    println(PI)

}